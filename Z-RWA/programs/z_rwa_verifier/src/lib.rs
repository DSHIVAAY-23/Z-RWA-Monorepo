#[program]
pub mod z_rwa_verifier {
    use super::*;

    pub fn verify_and_mint(
        ctx: Context<VerifyAndMint>,
        proof_a: [u8; 64],
        proof_b: [u8; 128],
        proof_c: [u8; 64],
        public_inputs: [[u8; 32]; 3],
    ) -> Result<()> {
        let mut verifier = Groth16Verifier::new(
            &proof_a,
            &proof_b,
            &proof_c,
            &public_inputs,
            &VERIFYING_KEY,
        ).map_err(|_| ErrorCode::ProofSetupFailed)?;

        verifier.verify().map_err(|_| ErrorCode::ProofVerificationFailed)?;

        // Emit event for indexing
        emit!(ProofVerified {
            wallet: ctx.accounts.user.key(),
            timestamp: Clock::get()?.unix_timestamp,
        });

        Ok(())
    }
}

#[derive(Accounts)]
pub struct VerifyAndMint<'info> {
    #[account(mut)]
    pub user: Signer<'info>,
    pub system_program: Program<'info, System>,
}

#[event]
pub struct ProofVerified {
    pub wallet: Pubkey,
    pub timestamp: i64,
}

#[error_code]
pub enum ErrorCode {
    #[msg("Proof setup failed")]
    ProofSetupFailed,
    #[msg("Proof verification failed on-chain")]
    ProofVerificationFailed,
}
